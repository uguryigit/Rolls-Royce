	SELECT [RSAF TABLE].BAEPO AS [SMS PO], 
	       [RSAF TABLE].POITEM AS ITEM, 
		   [RSAF TABLE].PART_NO, 
		   [RSAF TABLE].SERIAL, 
		   [RSAF TABLE].RECDATE, 
		   [RSAF TABLE].DESCRIPTION
	FROM [RSAF2].dbo.[RSAF TABLE]
	WHERE ((([RSAF TABLE].TYPE)='a') AND (([RSAF TABLE].RECDATE)>0) AND (([RSAF TABLE].DESPDATE) Is 
	Null))
--	ORDER BY [RSAF TABLE].RECDATE;
GO
--3
--DROP VIEW [ALL ITEMS WIP > THAN 150 DAYS];
CREATE VIEW [ALL ITEMS WIP > THAN 150 DAYS] AS
	SELECT DISTINCT  [RSAF TABLE].TYPE, 
	       [RSAF TABLE].SITE, 
		   Count([RSAF TABLE].SITE) AS CountOfSITE
	FROM [RSAF2].dbo.[RSAF TABLE]
	WHERE (((IIf([RSAF TABLE].[DESPDATE]IS NULL,GETDate()-[RSAF TABLE].[RECDATE],Null))>150) AND
	 (([RSAF TABLE].SITE)='A' Or ([RSAF TABLE].SITE)='B' Or ([RSAF TABLE].SITE)='F' Or ([RSAF TABLE].SITE)='M' Or
	 ([RSAF TABLE].SITE)='S') AND (([RSAF TABLE].DESPDATE) Is Null))
	GROUP BY [RSAF TABLE].TYPE, [RSAF TABLE].SITE
GO
