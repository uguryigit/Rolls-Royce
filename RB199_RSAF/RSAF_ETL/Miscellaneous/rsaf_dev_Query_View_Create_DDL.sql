	SELECT DISTINCT [RSAF TABLE].TYPE, 
	       [RSAF TABLE].SITE, 
		   Count([RSAF TABLE].SITE) AS CountOfSITE	
	FROM [RSAF2].dbo.[RSAF TABLE]
	WHERE (((IIf([RSAF TABLE].[DESPDATE] IS NULL,GetDate()-[RSAF TABLE].[RECDATE],Null))>90) AND 
	(([RSAF TABLE].SITE)='A' Or ([RSAF TABLE].SITE)='B' Or ([RSAF TABLE].SITE)='F' Or ([RSAF TABLE].SITE)='M' Or
	 ([RSAF TABLE].SITE)='S') AND (([RSAF TABLE].DESPDATE) Is Null))
	GROUP BY [RSAF TABLE].TYPE, [RSAF TABLE].SITE
GO 
