SELECT 'UPDATE DISTINCT [RAW RSAF] INNER JOIN RSAF ON [RAW RSAF].X = RSAF.X SET RSAF.RECDATE = Format$([RAW RSAF].[RECDATE],''@@-@@@-@@''), RSAF.PSIDATE = Format$([RAW RSAF].[PSIDATE],''@@-@@@-@@''), RSAF.DESPARDT = Format$([RAW RSAF].[DESPARDT],''@@-@@@-@@''), RSAF.RECDPART = Format$([RAW RSAF].[RECDPART],''@@-@@@-@@''), RSAF.INTOROS = Format$([RAW RSAF].[INTOROS],''@@-@@@-@@''), RSAF.ANSPSIDT = Format$([RAW RSAF].[ANSPSIDT],''@@-@@@-@@''), RSAF.DESPDATE = Format$([RAW RSAF].[DESPDATE],''@@-@@@-@@''), RSAF.BAESENT = Format$([RAW RSAF].[BAESENT],''@@-@@@-@@''), RSAF.PROMISE = Format$([RAW RSAF].[PROMISE],''@@-@@@-@@''), RSAF.PROMDATE = Format$([RAW RSAF].[PROMDATE],''@@-@@@-@@'');' AS SQLEXPRESSION
GO